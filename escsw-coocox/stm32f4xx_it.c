void DMA2_Stream0_IRQHandler(void)
{
	if ( DMA_GetFlagStatus( DMA2_Stream0, DMA_FLAG_TEIF0 ) ) {
		// some error occurred
		DMA_ClearFlag( DMA2_Stream0, DMA_FLAG_TEIF0 );
		// cleared and try again.
	}
	if ( DMA_GetFlagStatus( DMA2_Stream0, DMA_FLAG_TCIF0 ) ) {
		// transfer is complete
		DMA_ClearFlag( DMA2_Stream0, DMA_FLAG_TCIF0 );
		fastcounter++;
        fast_loop();

#ifdef GTDEBUG
        GPIO_ResetBits(GPIOE, GPIO_Pin_0);
#endif
	}
}

#ifdef GTDEBUG
void TIM2_IRQHandler(void)
{
	/* Clear TIM2 Capture compare interrupt pending bit */
	TIM_ClearITPendingBit(TIM2, TIM_IT_Update);
	GPIO_SetBits(GPIOE, GPIO_Pin_0);
}
#endif

void TIM3_IRQHandler(void)
{
	/* Clear TIM3 Capture compare interrupt pending bit */
	TIM_ClearITPendingBit(TIM3, TIM_IT_CC1);

	/* Get the Input Capture value */
	IC1Value = TIM_GetCapture1(TIM3);

	if (IC1Value != 0)
	{
		/* Duty cycle computation */
		DutyCycle = (TIM_GetCapture2(TIM3) * 1000) / IC1Value;

		/* Frequency computation */
		Frequency = SystemCoreClock / IC1Value;
	}
	else
	{
		DutyCycle = 0;
		Frequency = 0;
	}
}

